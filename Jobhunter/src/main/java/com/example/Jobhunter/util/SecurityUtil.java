    public String createToken(Authentication authentication) {

        Instant now = Instant.now(); // now: th·ªùi ƒëi·ªÉm ph√°t h√†nh token.
        Instant validity = now.plus(this.jwtExpiration, ChronoUnit.SECONDS); // validity: th·ªùi ƒëi·ªÉm h·∫øt h·∫°n = now +
                                                                             // jwtExpiration (v√≠ d·ª• 1h, 2h, ‚Ä¶)

        // @formatter:off
        //T·∫°o payload (claims)
        JwtClaimsSet claims = JwtClaimsSet.builder()
        .issuedAt(now) // iat: issued at
        .expiresAt(validity) // exp: expiration
        .subject (authentication.getName()) // sub: ƒë·ªãnh danh (th∆∞·ªùng l√† username)
        .claim ("hoidanit", authentication) //custom claim (t·ª± nh√©t th√™m data)
        .build();

        //T·∫°o header (thu·∫≠t to√°n k√Ω)
        JwsHeader jwsHeader = JwsHeader.with(JWT_Algorithm).build();

        //Encode th√†nh token
        // JwtEncoder (Spring Security cung c·∫•p) s·∫Ω:
        // L·∫•y header + payload.
        // D√πng jwtkey (secret key b·∫°n config) + thu·∫≠t to√°n HS512.
        // Sinh ra ch·ªØ k√Ω.
        // N·ªëi l·∫°i th√†nh JWT chu·∫©n: header.payload.signature
        return this.jwtEncoder.encode(JwtEncoderParameters.from(jwsHeader,
        claims)).getTokenValue();
    }

